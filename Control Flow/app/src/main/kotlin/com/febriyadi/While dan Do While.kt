package com.febriyadi

fun main (){
    //while
    var counter = 1
    while (counter <= 7){
        println("Hello, World!")
        counter++
    }
    //
    var counter1 = 8
    while (counter1 <= 7){
        println("Hello, World!")
        counter++
    }

    //Do while
    var counter2 = 1
    do {
        println("Hello, World!")
        counter++
    } while (counter2 <= 7)

    //interval loop
    var value = 'A'
    do {
        print(value)
    } while (value <= 'Z')
}